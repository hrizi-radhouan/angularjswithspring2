package com.mass.test2;

public class Variable {
	private String id;
	private String name;
	private String miniBudget;
	private String maxBudget;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMaxBudget() {
		return maxBudget;
	}
	public void setMaxBudget(String maxBudget) {
		this.maxBudget = maxBudget;
	}
	public String getMiniBudget() {
		return miniBudget;
	}
	public void setMiniBudget(String miniBudget) {
		this.miniBudget = miniBudget;
	}
	
}
